import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";

import getPageData from "@/functions/getPageData";

import { notFound } from "next/navigation";
import { FromLayoutToPageLoader } from "@/components/loaders/FromLayoutToPageLoader";
import { ContentPageTypeType, DataExtended } from "@/types/types";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "This is Page. The - Page.",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
  params,
}: Readonly<{
  children: React.ReactNode;
  params: Promise<{ web?: string[] }>;
}>) {

  const { web } = await params;
  const slug = web?.join("/") || "home";
  let generalData : DataExtended | null = null;

  generalData = await getPageData(`/api/pages?filters[slug]=${slug}&populate=*`);

  let contentPageType: ContentPageTypeType = "page";

  if (!generalData) {
    generalData = await getPageData(`/api/posts?filters[slug]=${slug}&populate=*`);
    contentPageType = "post";
  }

  if (!generalData) return notFound();

  const urlItems = await getMenuItems(`http://srv-strapi:1337/api/pages?sort=pozycja:asc`);

  if (!urlItems) return null;

  return (
    <html lang="pl">
      <body>
        <FromLayoutToPageLoader
          slug={slug}
          generalData={generalData}
          menu={urlItems}
          contentPageType={contentPageType}
        >
          {children}
        </FromLayoutToPageLoader>
      </body>
    </html>
  );
}
